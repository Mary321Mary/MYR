package task6;
public class Backpack{
    private double maxW;
    private double maxC;
    
    public Backpack(){
    }
    
    public void setMaxW(double maxW){
        this.maxW = maxW; 
    } 
    
    public double getMaxW(){
        return this.maxW;
    }
    
    public void setMaxC(double maxC){
        this.maxC = maxC; 
    } 
    
    public double getMaxC(){
        return this.maxC;
    } 
}
